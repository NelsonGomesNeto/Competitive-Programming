#include <stdio.h>

int main()
{
  int tam, ler, min = 0, limit = 1, maior = 0, posi, quanti[10]; scanf("%d", &tam);
  float num[tam];
  for (ler = 0; ler < tam; ler ++)
  {
    scanf("%f", &num[ler]);
  }
  for (ler = 0; ler < 10; ler ++)
  {
    quanti[ler] = 0;
  }
  for (limit = 1, min = 0, posi = 0; limit <= 10; posi ++, limit ++, min ++)
  {
    for (ler = 0; ler < tam; ler ++)
    {
      if (num[ler] > min && num[ler] <= limit)
      {
        quanti[posi] ++;
      }
      if (min == 0 && num[ler] == 0)
      {
        quanti[posi] ++;
      }
    }
    if (maior < quanti[posi])
    {
      maior = quanti[posi];
    }
  }
  for (ler = 0; ler < 10; ler ++)
  {
    quanti[ler] = (20 * quanti[ler]) / maior;
  }
  printf("     ^\n");
  for (ler = 10; ler > 0; ler --)
  {
    if (ler < 10)
    {
      printf(" ");
    }
    if (ler % 5 == 0)
    {
      printf("  %d |", ler);
    }
    else
    {
      printf("    |");
    }
    while (quanti[ler - 1] > 0)
    {
      printf("*");
      quanti[ler - 1] --;
    }
    printf("\n");
  }
  printf("   0 +----+----+----+----+>\n");
  printf("     ");
  int espaco, divi;
  for (ler = 0; ler < 5; ler ++)
  {
    printf("%d", (maior * ler) / 4);
    divi = (maior * (ler + 1)) / 4; espaco = 4;
    while (divi > 10)
    {
      divi /= 10;
      espaco --;
    }
    while (espaco > 0)
    {
      printf(" ");
      espaco --;
    }
  }
  printf("\n");
  return(0);
}
